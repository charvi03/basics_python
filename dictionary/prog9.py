# inserting
Dict = {}
print("Empty Dictionary: ")
print(Dict)
Dict[0] = "k"
Dict[2] = 'For'
Dict[3] = 1
print("\nDictionary after adding 3 elements: ")
print(Dict)
# adding set of values
Dict['Value_set'] = 2, 3, 4
print("\nDictionary after adding 3 elements: ")
print(Dict)
# updating values
Dict[2] = 'Welcome'
print("\nUpdated key value: ")
print(Dict)
# nested values
Dict[5] = {'Nested': {1: 'Life', 2: 'Geeks'}}
print("\nAdding a Nested Key: ")
print(Dict)
